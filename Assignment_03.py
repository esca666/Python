#creating a list and removing the item from the list 
my_list = [45,56,24,78,10,34]

#removing the item from the list 
my_list.remove(78) #as an example we are removing number 78
print(my_list)

#now, printing the maximum and minimum value of an list
max_value = max(my_list)
min_value = min(my_list)
print('maximum value : ' , max_value)
print('minimum value: ' , min_value)
